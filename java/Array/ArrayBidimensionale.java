public class ArrayBidimensionale {
    public static void main(String[] args) {
        
        // Dichiarazione e inizializzazione di un array bidimensionale di interi
        int[][] matrice = {{0, 0, 0, 0}, {0, 1, 2, 3}, {0, 2, 4, 6}};
        
        // Stampa di tutti gli elementi dell'array bidimensionale
        System.out.println(matrice[0][0] + " " + matrice[0][1] + " " + matrice[0][2] + " " + matrice[0][3]);
        System.out.println(matrice[1][0] + " " + matrice[1][1] + " " + matrice[1][2] + " " + matrice[1][3]);
        System.out.println(matrice[2][0] + " " + matrice[2][1] + " " + matrice[2][2] + " " + matrice[2][3]);
    }
}
